    private void btnRestaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRestaActionPerformed
        if (!txtPantalla.getText().equals("")) {
            operacion.setNumero1(Float.parseFloat(txtPantalla.getText()));
            operacion.resta();
            if (operacion.getOperacionAntes() != '-') {
                operacion.setOperacionAntes('-');
            }
            operacionRealizada = true;
        }
    }//GEN-LAST:event_btnRestaActionPerformed

    private void btnMultiplicacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMultiplicacionActionPerformed
        // TODO add your handling code here:
        if (!txtPantalla.getText().equals("")) {
            if (operacion.getResultados() == 0) {
                operacion.setResultados(1);
            }
            operacion.setNumero1(Float.parseFloat(txtPantalla.getText()));
            operacion.multiplicacion();
            if (operacion.getOperacionAntes() != 'x') {
                operacion.setOperacionAntes('x');
            }
            operacionRealizada = true;
        }
    }//GEN-LAST:event_btnMultiplicacionActionPerformed

    private void btnDividirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDividirActionPerformed
        if (!txtPantalla.getText().equals("")) {
            if (operacion.getResultados() == 0) {
                operacion.setResultados(1);
            }
            operacion.setNumero1(Float.parseFloat(txtPantalla.getText()));
            operacion.divicion();
            if (operacion.getOperacionAntes() != '/') {
                operacion.setOperacionAntes('/');
            }
            operacionRealizada = true;
        }
    }//GEN-LAST:event_btnDividirActionPerformed

