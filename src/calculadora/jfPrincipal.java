    public boolean existenciaPunto() {
        // Verifica que no exita un punto antes
        boolean respuesta = true;
        if (!txtPantalla.getText().equals("")) {
            for (int i = 0; i < txtPantalla.getText().length(); i++) {
                char letra = txtPantalla.getText().charAt(i);
                if (letra == '.') {
                    respuesta = false;
                    break;
                }
            }
        } else {
            respuesta = false;
        }

        return respuesta;
    }

